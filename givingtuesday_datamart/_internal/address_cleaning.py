USPS_KEYS = sorted(USPS_MAP.keys(), key=len, reverse=True)
ADDRESS_PATTERN = re.compile(
    r'\b(' + '|'.join(re.escape(k) for k in USPS_KEYS) + r')\b\.?',
    re.IGNORECASE,
)


def create_clean_address(
    row,
    address_part_columns=('address1_key', 'address2_key', 'addresscity_key', 'addressstate_key'),
):
    parts = [row[col] for col in address_part_columns]
    name = " ".join([p.strip() for p in parts if p.strip()])

    if not isinstance(name, str):
        return ""

    name = name.upper().strip()
    name = ADDRESS_PATTERN.sub(
        lambda x: USPS_MAP[x.group().upper().replace('.', '')],
        name,
    )
    name = re.sub(r'[^\w\s]', '', name)
    name = re.sub(r'\s+', ' ', name).strip()
    return name
